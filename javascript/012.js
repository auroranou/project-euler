/* ------
The sequence of triangle numbers is generated by adding the natural numbers. So the 7th triangle number would be 1 + 2 + 3 + 4 + 5 + 6 + 7 = 28. The first ten terms would be:

1, 3, 6, 10, 15, 21, 28, 36, 45, 55, ...

Let us list the factors of the first seven triangle numbers:

 1: 1
 3: 1,3
 6: 1,2,3,6
10: 1,2,5,10
15: 1,3,5,15
21: 1,3,7,21
28: 1,2,4,7,14,28
We can see that 28 is the first triangle number to have over five divisors.

What is the value of the first triangle number to have over five hundred divisors?
------ */

var triangles = [];

// create array of triangle numbers, stopping at arbitrary point (16,000)
for (var i = 0; i < 16000; i++) {
  if (!triangles.length) {
    triangles.push(i);
  } else {
    var nextNum = triangles[triangles.length - 1];
    triangles.push(nextNum + i);
  }
}

function getFactors(num) {
  var factors = [];
  var max = Math.round(Math.sqrt(num));

  // create array of factors; don't duplicate squares
  for (var i = 1; i < max; i++) {
    if (num % i === 0) {
      var otherFactor = num / i;
      factors.push(i);
      if (otherFactor !== i) factors.push(otherFactor);
    }
  }
  return factors;
}

var factors = triangles.map(function(t) { return [t, getFactors(t).length]; });
var moreThan500 = factors.filter(function(f) { return f[1] >= 500; });
